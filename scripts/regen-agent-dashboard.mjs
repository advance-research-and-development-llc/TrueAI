#!/usr/bin/env node
// scripts/regen-agent-dashboard.mjs
//
// Regenerate docs/AGENT_DASHBOARD.md from live GitHub data via `gh`.
// Pure local generation — no third-party analytics; respects the
// local-first design constraint.
//
// Inputs (env): OWNER, REPO. Defaults to advance-research-and-development-llc/TrueAI.
// Output: writes docs/AGENT_DASHBOARD.md.
//
// Usage (typically invoked by .github/workflows/agent-dashboard.yml):
//   node scripts/regen-agent-dashboard.mjs

import { execSync } from 'node:child_process';
import { writeFileSync, existsSync, readFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const OUT = resolve(REPO_ROOT, 'docs/AGENT_DASHBOARD.md');

const OWNER = process.env.OWNER || 'advance-research-and-development-llc';
const REPO  = process.env.REPO  || 'TrueAI';
const SLUG  = `${OWNER}/${REPO}`;

function gh(args) {
  try {
    return execSync(`gh ${args}`, { encoding: 'utf8', stdio: ['ignore', 'pipe', 'pipe'] });
  } catch (err) {
    console.error(`gh ${args} failed: ${err.message}`);
    return '';
  }
}

// --- Open copilot-fix issues by secondary label ---------------------------
const LABELS = [
  'lint-error',
  'test-failure',
  'android-lint',
  'dependency-vuln',
  'coverage-gap',
  'compatibility',
  'security',
  'runtime-crash',
];

const counts = {};
for (const lab of LABELS) {
  const out = gh(`issue list --repo ${SLUG} --label copilot-fix,${lab} --state open --limit 200 --json number --jq 'length'`);
  counts[lab] = parseInt(out.trim() || '0', 10);
}
const totalOpen = parseInt(
  gh(`issue list --repo ${SLUG} --label copilot-fix --state open --limit 200 --json number --jq 'length'`).trim() || '0',
  10,
);

// --- Recent agent PRs ------------------------------------------------------
const recentJson = gh(`pr list --repo ${SLUG} --state closed --limit 30 --json number,title,author,mergedAt,mergeCommit`);
let recent;
try { recent = JSON.parse(recentJson || '[]'); } catch { recent = []; }
const agentMerged = recent.filter(p =>
  p.author?.login === 'Copilot' || p.author?.login === 'copilot-swe-agent[bot]' || p.author?.login === 'github-actions[bot]'
).filter(p => p.mergedAt);

// --- Coverage trend (best-effort, from coverage-summary.json if present) --
let covLine = '_(no recent coverage report committed)_';
const covPath = resolve(REPO_ROOT, 'coverage/coverage-summary.json');
if (existsSync(covPath)) {
  try {
    const c = JSON.parse(readFileSync(covPath, 'utf8')).total;
    covLine = `lines **${c.lines.pct.toFixed(2)}%** · branches **${c.branches.pct.toFixed(2)}%** · functions **${c.functions.pct.toFixed(2)}%** · statements **${c.statements.pct.toFixed(2)}%**`;
  } catch { /* ignore */ }
}

const generatedAt = new Date().toISOString();

const md = `# Agent activity dashboard

> _Auto-regenerated by [\`scripts/regen-agent-dashboard.mjs\`](../scripts/regen-agent-dashboard.mjs)._
> _Last generated: \`${generatedAt}\`._
>
> Pure local-first analytics — no third-party services. Source data is
> the public GitHub Issues / PRs API. Do not edit by hand; the next
> nightly run will overwrite your changes.

## Open \`copilot-fix\` queue

**Total open: ${totalOpen} / 25 (throttle cap)**

| Defect class | Open issues |
|---|---:|
${LABELS.map(l => `| \`${l}\` | ${counts[l]} |`).join('\n')}

## Recent autonomous merges

Last ${agentMerged.length} agent-authored PRs that merged into \`main\`:

${agentMerged.length === 0
  ? '_(none in the recent window)_'
  : agentMerged.slice(0, 15).map(p =>
      `- [#${p.number}](https://github.com/${SLUG}/pull/${p.number}) — ${p.title} _(merged ${p.mergedAt?.slice(0,10)})_`
    ).join('\n')
}

## Coverage snapshot

${covLine}

CI floor (from \`vitest.config.ts\`): see the \`thresholds\` block.

## Operator commands

- \`gh issue list --repo ${SLUG} --label copilot-fix --state open\`
- \`gh workflow run coverage-dispatch.yml --repo ${SLUG}\`
- \`gh workflow run dependency-scan.yml --repo ${SLUG}\`
- \`scripts/verify-owner-setup.sh --owner ${OWNER} --repo ${REPO}\`

See [\`docs/AGENT_OPERATIONS.md\`](./AGENT_OPERATIONS.md) for the full runbook.
`;

writeFileSync(OUT, md, 'utf8');
console.log(`Wrote ${OUT}`);
