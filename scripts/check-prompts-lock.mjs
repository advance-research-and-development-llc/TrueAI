#!/usr/bin/env node
// scripts/check-prompts-lock.mjs
//
// Audit-trail for the canonical agent prompt fragments in
// .github/copilot/PROMPTS.md.
//
// Why
//   Dispatcher workflows (auto-lint-fix.yml, codeql-autofix.yml,
//   android-audit.yml, dependency-scan.yml, coverage-dispatch.yml ...)
//   embed named fragments from PROMPTS.md verbatim into the issues
//   they open. A silent edit to a fragment changes what every future
//   dispatched issue tells Copilot to do. A lockfile makes that
//   change explicit: change the prompt → bump the hash. PRs that
//   touch PROMPTS.md without bumping .github/copilot/PROMPTS.lock
//   fail this check.
//
// How
//   1. Parse PROMPTS.md, splitting on `^## Fragment: ` headers and
//      treating the prelude (`## Universal hard constraints`) as a
//      pseudo-fragment named `__universal__`.
//   2. SHA-256 each fragment body (the lines from after the header
//      up to the next `^## ` or EOF), trimmed.
//   3. Compare against the lock file.
//
// Modes
//   default          → verify; exit 1 on mismatch
//   --write          → (re)generate the lock file from current PROMPTS.md
//   --self-test      → exercise the parser/hasher against a fixture
//
// Pure Node stdlib — no third-party deps.

import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { createHash } from 'node:crypto'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, '..')
const PROMPTS_PATH = resolve(REPO_ROOT, '.github/copilot/PROMPTS.md')
const LOCK_PATH = resolve(REPO_ROOT, '.github/copilot/PROMPTS.lock')

const FRAG_HEADER = /^## Fragment:\s+`([^`]+)`\s*$/
const UNIVERSAL_HEADER = /^## Universal hard constraints/
const ANY_H2 = /^## /

function parseFragments(md) {
  const lines = md.split('\n')
  const fragments = new Map() // name → { startLine, body }

  let current = null

  function flush(endIdx) {
    if (!current) return
    const body = lines.slice(current.bodyStart, endIdx).join('\n').trim()
    fragments.set(current.name, body)
    current = null
  }

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i]
    const fragMatch = line.match(FRAG_HEADER)
    const isUniversal = UNIVERSAL_HEADER.test(line)
    const isAnyH2 = ANY_H2.test(line)

    if (fragMatch) {
      flush(i)
      current = { name: fragMatch[1], bodyStart: i + 1 }
    } else if (isUniversal) {
      flush(i)
      current = { name: '__universal__', bodyStart: i + 1 }
    } else if (isAnyH2 && current) {
      flush(i)
    }
  }
  flush(lines.length)
  return fragments
}

function hashBody(body) {
  return createHash('sha256').update(body, 'utf8').digest('hex').slice(0, 16)
}

function buildLock(fragments) {
  // Stable ordering by fragment name for deterministic diffs.
  const sorted = [...fragments.entries()].sort(([a], [b]) => a.localeCompare(b))
  const out = [
    '# .github/copilot/PROMPTS.lock',
    '#',
    '# Auto-generated by scripts/check-prompts-lock.mjs --write.',
    '# DO NOT hand-edit. Update the corresponding fragment in PROMPTS.md',
    '# and re-run `npm run check:prompts -- --write`.',
    '#',
    '# Each line is "<name> <sha256-prefix>". The hash covers the',
    '# trimmed fragment body (everything between the `## Fragment:`',
    '# header and the next `## ` header).',
    '',
  ]
  for (const [name, body] of sorted) {
    out.push(`${name} ${hashBody(body)}`)
  }
  return out.join('\n') + '\n'
}

function parseLock(text) {
  const map = new Map()
  for (const raw of text.split('\n')) {
    const line = raw.trim()
    if (!line || line.startsWith('#')) continue
    const [name, hash] = line.split(/\s+/)
    if (name && hash) map.set(name, hash)
  }
  return map
}

// --- self-test --------------------------------------------------------------

if (process.argv.includes('--self-test')) {
  const fixture = `# Title
## Universal hard constraints (always included)

Some constraints body line A
- [ ] check 1

## Fragment: \`fix-codeql\`

Body for codeql.

## Fragment: \`fix-lint\`

Body for lint.
multi-line.

## Notes (not a fragment)

This should NOT be parsed.
`
  const frags = parseFragments(fixture)
  const want = ['__universal__', 'fix-codeql', 'fix-lint']
  const got = [...frags.keys()].sort()
  let failed = 0
  if (JSON.stringify(got) !== JSON.stringify(want.sort())) {
    console.error(`✗ fragment names mismatch: want=${want} got=${got}`)
    failed++
  } else {
    console.log(`✓ parsed 3 fragments`)
  }
  // Hash should be stable.
  const h1 = hashBody(frags.get('fix-codeql'))
  const h2 = hashBody(frags.get('fix-codeql'))
  if (h1 !== h2 || h1.length !== 16) {
    console.error(`✗ hashBody not stable / wrong length: ${h1} vs ${h2}`)
    failed++
  } else {
    console.log(`✓ hash stable (${h1})`)
  }
  // "Notes" must NOT be in fragments.
  if (frags.has('Notes (not a fragment)')) {
    console.error('✗ Notes header was misparsed as a fragment')
    failed++
  } else {
    console.log('✓ non-fragment H2 ignored')
  }
  process.exit(failed ? 1 : 0)
}

// --- main -------------------------------------------------------------------

if (!existsSync(PROMPTS_PATH)) {
  console.error(`✗ ${PROMPTS_PATH} not found`)
  process.exit(2)
}

const md = readFileSync(PROMPTS_PATH, 'utf8')
const fragments = parseFragments(md)

if (fragments.size === 0) {
  console.error('✗ Parsed zero fragments from PROMPTS.md — header format may have changed.')
  process.exit(2)
}

const want = process.argv.includes('--write')
if (want) {
  writeFileSync(LOCK_PATH, buildLock(fragments))
  console.log(`✓ wrote ${LOCK_PATH} (${fragments.size} fragments)`)
  process.exit(0)
}

if (!existsSync(LOCK_PATH)) {
  console.error(`✗ ${LOCK_PATH} not found. Run: npm run check:prompts -- --write`)
  process.exit(1)
}

const lock = parseLock(readFileSync(LOCK_PATH, 'utf8'))
const violations = []
const seen = new Set()

for (const [name, body] of fragments) {
  seen.add(name)
  const want = lock.get(name)
  const got = hashBody(body)
  if (!want) {
    violations.push(`+ NEW fragment "${name}" (hash ${got}) is not in the lock file`)
  } else if (want !== got) {
    violations.push(`! CHANGED fragment "${name}": locked=${want} actual=${got}`)
  }
}
for (const name of lock.keys()) {
  if (!seen.has(name)) {
    violations.push(`- REMOVED fragment "${name}" is in the lock file but not in PROMPTS.md`)
  }
}

if (violations.length === 0) {
  console.log(`✓ ${fragments.size} prompt fragment(s) match PROMPTS.lock`)
  process.exit(0)
}

console.error('✗ Prompt fragment drift detected:')
for (const v of violations) console.error(`  ${v}`)
console.error('')
console.error('If these changes are intentional, regenerate the lock with:')
console.error('  npm run check:prompts -- --write')
console.error('')
console.error('and include the lock-file diff in the same PR so reviewers')
console.error('can see exactly which fragments changed.')
process.exit(1)
