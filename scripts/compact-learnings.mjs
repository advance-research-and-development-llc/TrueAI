#!/usr/bin/env node
// scripts/compact-learnings.mjs
//
// One-shot compactor for `.github/copilot/LEARNINGS.md`.
//
// Goal: keep the live LEARNINGS.md small enough to fit in every agent
// session's working context (target ≤ 100 body lines, hard cap on max
// entries) without losing history. Older / noise entries get moved
// into `.github/copilot/LEARNINGS.archive.md`.
//
// Rules:
//   1. Drop "(No explicit lessons recorded.)" stub entries entirely —
//      they were emitted by an older version of learnings-ingest.yml
//      and have no value.
//   2. Move every dated entry older than ARCHIVE_AFTER_DAYS (default
//      90) into the archive file, newest-first.
//   3. If after (1) and (2) the live file is still longer than
//      MAX_LIVE_ENTRIES (default 12), move the oldest surviving
//      entries to the archive until under the cap.
//
// Modes:
//   default       --  apply changes, write both files
//   --dry-run     --  print plan, do not write
//   --self-test   --  run an in-memory parser/round-trip test
//
// Pure stdlib: works in the same Node version CI uses, no deps.

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';

const REPO_ROOT = process.cwd();
const LIVE = path.join(REPO_ROOT, '.github', 'copilot', 'LEARNINGS.md');
const ARCHIVE = path.join(REPO_ROOT, '.github', 'copilot', 'LEARNINGS.archive.md');

const ARCHIVE_AFTER_DAYS = Number(process.env.ARCHIVE_AFTER_DAYS || 90);
const MAX_LIVE_ENTRIES = Number(process.env.MAX_LIVE_ENTRIES || 12);
const STUB_PATTERN = /\(No explicit lessons recorded\.\)/;

const ARCHIVE_HEADER =
  '# Project learnings — archive\n\n' +
  '> Auto-generated by `scripts/compact-learnings.mjs`. Older entries\n' +
  "> live here so the agent's working set stays small. Newest archived\n" +
  '> entry first.\n\n---\n\n';

/**
 * Split a learnings markdown body into (preamble, entries[]).
 *
 * An entry is a section that starts with `## YYYY-MM-DD — ...` and
 * runs up to the next `## ` or end-of-file. The horizontal rule
 * `---` separators are part of each entry block (kept on the entry
 * for round-trippability).
 */
function parseLearnings(text) {
  const lines = text.split('\n');
  const entries = [];
  const preamble = [];

  let inPreamble = true;
  let current = null;

  for (const line of lines) {
    const m = line.match(/^##\s+(\d{4}-\d{2}-\d{2})\b\s*[—\-]\s*(.+)$/);
    if (m) {
      if (current) entries.push(current);
      current = { date: m[1], title: m[2].trim(), lines: [line] };
      inPreamble = false;
      continue;
    }
    if (inPreamble) {
      preamble.push(line);
    } else if (current) {
      current.lines.push(line);
    }
  }
  if (current) entries.push(current);

  // Trim trailing blank lines + dangling `---` from each entry's tail
  // so we can recompose deterministically.
  for (const e of entries) {
    while (e.lines.length && e.lines.at(-1).trim() === '') e.lines.pop();
    if (e.lines.at(-1) === '---') e.lines.pop();
    while (e.lines.length && e.lines.at(-1).trim() === '') e.lines.pop();
  }

  return { preamble: preamble.join('\n').replace(/\n+$/, '') + '\n', entries };
}

function renderEntries(entries) {
  return entries.map((e) => e.lines.join('\n').trimEnd() + '\n\n---\n').join('\n');
}

function isStub(entry) {
  return entry.lines.some((l) => STUB_PATTERN.test(l));
}

function ageDays(entry, now) {
  const t = Date.parse(entry.date + 'T00:00:00Z');
  if (Number.isNaN(t)) return Infinity;
  return Math.floor((now - t) / 86400000);
}

function compact({ liveText, archiveText, now }) {
  const live = parseLearnings(liveText);
  const archive = archiveText
    ? parseLearnings(archiveText)
    : { preamble: ARCHIVE_HEADER, entries: [] };

  const droppedStubs = [];
  const archived = [];
  const kept = [];

  for (const e of live.entries) {
    if (isStub(e)) {
      droppedStubs.push(e);
      continue;
    }
    if (ageDays(e, now) > ARCHIVE_AFTER_DAYS) {
      archived.push(e);
      continue;
    }
    kept.push(e);
  }

  // Cap the live file by entry count (newest-first → drop tail).
  kept.sort((a, b) => (a.date < b.date ? 1 : a.date > b.date ? -1 : 0));
  while (kept.length > MAX_LIVE_ENTRIES) {
    archived.push(kept.pop());
  }

  // Merge with existing archive, dedupe by (date, title), newest-first.
  const allArchive = [...archived, ...archive.entries];
  const seen = new Set();
  const archiveOut = [];
  allArchive.sort((a, b) => (a.date < b.date ? 1 : a.date > b.date ? -1 : 0));
  for (const e of allArchive) {
    const key = `${e.date}::${e.title}`;
    if (seen.has(key)) continue;
    seen.add(key);
    archiveOut.push(e);
  }

  const liveOut = live.preamble + '\n' + renderEntries(kept);
  const archiveDoc =
    (archive.preamble || ARCHIVE_HEADER).trimEnd() + '\n\n' + renderEntries(archiveOut);

  return {
    liveOut,
    archiveDoc,
    stats: {
      droppedStubs: droppedStubs.length,
      archived: archived.length,
      kept: kept.length,
      archiveTotal: archiveOut.length,
    },
  };
}

function selfTest() {
  const fixture = `# Project learnings

intro line.

---

## 2026-04-30 — PR #200: Real lesson

- Real bullet.

---

## 2026-04-29 — PR #199: Stub PR

- (No explicit lessons recorded.) PR title: _Stub PR_.

---

## 2025-01-01 — PR #1: Old lesson

- Older than 90 days.

---
`;
  const now = Date.parse('2026-05-01T00:00:00Z');
  const { liveOut, archiveDoc, stats } = compact({
    liveText: fixture,
    archiveText: '',
    now,
  });

  const checks = [
    ['stub dropped', stats.droppedStubs === 1],
    ['old archived', stats.archived === 1],
    ['recent kept', stats.kept === 1],
    ['live has real lesson', /Real lesson/.test(liveOut)],
    ['live drops stub', !STUB_PATTERN.test(liveOut)],
    ['live drops old', !/Old lesson/.test(liveOut)],
    ['archive has old', /Old lesson/.test(archiveDoc)],
    ['archive omits stub', !STUB_PATTERN.test(archiveDoc)],
  ];
  let ok = true;
  for (const [name, pass] of checks) {
    console.log(`${pass ? 'ok' : 'FAIL'}  ${name}`);
    if (!pass) ok = false;
  }
  return ok;
}

function main() {
  const args = new Set(process.argv.slice(2));

  if (args.has('--self-test')) {
    process.exit(selfTest() ? 0 : 1);
  }

  if (!fs.existsSync(LIVE)) {
    console.error(`error: ${LIVE} does not exist.`);
    process.exit(2);
  }

  const liveText = fs.readFileSync(LIVE, 'utf8');
  const archiveText = fs.existsSync(ARCHIVE) ? fs.readFileSync(ARCHIVE, 'utf8') : '';

  const { liveOut, archiveDoc, stats } = compact({
    liveText,
    archiveText,
    now: Date.now(),
  });

  console.log(
    `compacting LEARNINGS.md: dropped ${stats.droppedStubs} stub(s), ` +
      `archived ${stats.archived}, kept ${stats.kept} live, ` +
      `archive total ${stats.archiveTotal}.`,
  );

  if (args.has('--dry-run')) {
    console.log('--dry-run: not writing.');
    return;
  }

  fs.writeFileSync(LIVE, liveOut, 'utf8');
  fs.writeFileSync(ARCHIVE, archiveDoc, 'utf8');
  console.log(`wrote ${path.relative(REPO_ROOT, LIVE)} and ${path.relative(REPO_ROOT, ARCHIVE)}.`);
}

main();
