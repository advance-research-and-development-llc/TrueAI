#!/usr/bin/env node
// scripts/gen-tool-docs.mjs
//
// §V — Tool registry → docs autogen.
//
// Source-of-truth: `src/lib/agent/tool-registry.ts` (the BUILTIN_TOOLS array
// of `ToolDefinition` literals). This script parses each `ToolDefinition`
// literal block textually (no Zod execution, no TS compiler dependency) and
// emits `docs/AGENT_TOOLS.md` with one row per tool: name, description,
// `requiresNetwork`, `requiresCredential`, link to source line.
//
// Run as `npm run docs:tools`. CI variant `npm run check:tool-docs` runs
// the same parse and fails if the on-disk markdown drifts from what would
// be generated, mirroring the prompts-lock / instructions-drift pattern.
//
// Pure stdlib — no extra deps. Matches the rest of `scripts/*.mjs`.

import { readFile, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import path from 'node:path'

const __filename = fileURLToPath(import.meta.url)
const repoRoot = path.resolve(path.dirname(__filename), '..')
const REGISTRY_PATH = path.join(repoRoot, 'src/lib/agent/tool-registry.ts')
const DOCS_PATH = path.join(repoRoot, 'docs/AGENT_TOOLS.md')

const HEADER_NOTE =
  '<!-- AUTOGENERATED by scripts/gen-tool-docs.mjs from src/lib/agent/tool-registry.ts. Do not edit by hand. -->'

/**
 * Parse `tool-registry.ts` for `ToolDefinition` const declarations and
 * extract their {name, description, requiresNetwork, requiresCredential}
 * fields. Tracks the 1-based source line of the `name:` field for
 * link-back. Throws on malformed input rather than silently dropping a
 * tool.
 */
export function parseRegistry(source) {
  const lines = source.split('\n')
  const tools = []

  // Match `const fooTool: ToolDefinition = {` (allow generic args + spacing).
  const declRe = /^const\s+(\w+):\s*ToolDefinition(?:<[^>]*>)?\s*=\s*\{/

  for (let i = 0; i < lines.length; i++) {
    const m = declRe.exec(lines[i])
    if (!m) continue

    // Extract the literal block by tracking brace depth from the opening `{`.
    let depth = 0
    let started = false
    let endLine = -1
    const blockLines = []
    for (let j = i; j < lines.length; j++) {
      const ln = lines[j]
      blockLines.push(ln)
      for (const ch of ln) {
        if (ch === '{') {
          depth++
          started = true
        } else if (ch === '}') {
          depth--
          if (started && depth === 0) {
            endLine = j
            break
          }
        }
      }
      if (endLine !== -1) break
    }
    if (endLine === -1) {
      throw new Error(
        `gen-tool-docs: unbalanced braces in ToolDefinition starting at line ${i + 1}`,
      )
    }

    const block = blockLines.join('\n')

    const nameMatch = /\bname:\s*'([^']+)'/.exec(block)
    if (!nameMatch) {
      throw new Error(
        `gen-tool-docs: ToolDefinition starting at line ${i + 1} has no string name`,
      )
    }

    // Description may span multiple lines; capture single- or
    // multi-line strings. Strip outer quotes + leading whitespace.
    const descMatch = /\bdescription:\s*\n?\s*'([\s\S]*?)'\s*,/.exec(block)
    const description = descMatch
      ? descMatch[1].replace(/\s+/g, ' ').trim()
      : ''

    const requiresNetwork = /\brequiresNetwork:\s*true\b/.test(block)
    const requiresCredential = /\brequiresCredential:\s*true\b/.test(block)

    // Line number of the `name:` field (1-based) for link-back.
    let nameLine = i + 1
    for (let j = i; j <= endLine; j++) {
      if (/\bname:\s*'/.test(lines[j])) {
        nameLine = j + 1
        break
      }
    }

    tools.push({
      symbol: m[1],
      name: nameMatch[1],
      description,
      requiresNetwork,
      requiresCredential,
      sourceLine: nameLine,
    })
    i = endLine
  }

  // Stable order: keep source order.
  return tools
}

export function renderMarkdown(tools) {
  const lines = []
  lines.push('# Agent Tools')
  lines.push('')
  lines.push(HEADER_NOTE)
  lines.push('')
  lines.push(
    'This page is generated from the canonical tool registry at',
    '[`src/lib/agent/tool-registry.ts`](../src/lib/agent/tool-registry.ts).',
    'It lists every built-in tool the local-first agent loop exposes,',
    'along with the gating flags that decide whether the tool is handed',
    'to a hosted LLM at all.',
  )
  lines.push('')
  lines.push('## Built-in tools')
  lines.push('')
  lines.push('| Tool | Description | Network | Credential | Source |')
  lines.push('| --- | --- | :---: | :---: | --- |')
  for (const t of tools) {
    const network = t.requiresNetwork ? '✅' : '—'
    const credential = t.requiresCredential ? '✅' : '—'
    const src = `[L${t.sourceLine}](../src/lib/agent/tool-registry.ts#L${t.sourceLine})`
    const desc = t.description.replace(/\|/g, '\\|')
    lines.push(`| \`${t.name}\` | ${desc} | ${network} | ${credential} | ${src} |`)
  }
  lines.push('')
  lines.push('## Gating semantics')
  lines.push('')
  lines.push(
    '- **Network** — tool may make outbound network calls. The registry',
    "  refuses to expose any network-gated tool unless the runtime",
    '  confirms a hosted-LLM credential is configured.',
    '- **Credential** — implies Network; the tool itself reads or',
    '  forwards a credential value. Same gating as Network.',
  )
  lines.push('')
  lines.push(
    'Both gates fall *closed* on errors from `secureStorage` so a',
    'misconfigured device degrades to a fully local tool set rather than',
    'silently leaking a stale credential.',
  )
  lines.push('')
  return lines.join('\n')
}

async function main() {
  const checkOnly = process.argv.includes('--check')
  const source = await readFile(REGISTRY_PATH, 'utf8')
  const tools = parseRegistry(source)
  if (tools.length === 0) {
    console.error('gen-tool-docs: no ToolDefinitions found in registry')
    process.exit(2)
  }
  const rendered = renderMarkdown(tools)

  if (checkOnly) {
    let onDisk = ''
    try {
      onDisk = await readFile(DOCS_PATH, 'utf8')
    } catch {
      console.error(
        `gen-tool-docs: ${path.relative(repoRoot, DOCS_PATH)} is missing. Run \`npm run docs:tools\`.`,
      )
      process.exit(1)
    }
    if (onDisk !== rendered) {
      console.error(
        `gen-tool-docs: ${path.relative(repoRoot, DOCS_PATH)} is out of date. Run \`npm run docs:tools\`.`,
      )
      process.exit(1)
    }
    console.log(`gen-tool-docs: ${tools.length} tools, docs in sync.`)
    return
  }

  await writeFile(DOCS_PATH, rendered, 'utf8')
  console.log(
    `gen-tool-docs: wrote ${path.relative(repoRoot, DOCS_PATH)} (${tools.length} tools).`,
  )
}

const isMain =
  process.argv[1] && path.resolve(process.argv[1]) === __filename
if (isMain) {
  main().catch((err) => {
    console.error(err.stack || err.message || String(err))
    process.exit(1)
  })
}
